package xx;

public class SumMul {
    public static void main(String[] args) {
        int i,j;
        for(i=1;i<10;i++){
            for(j=i;j<10;j++){

                System.out.println(i+" * "+j+" = "+i*j+"\t");
            }
            System.out.println("\n");
        }
    }
}
